    private void tableVuelosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableVuelosMouseClicked
        int seleccionar = tableVuelos.rowAtPoint(evt.getPoint());
        txtIdVuelo.setText(String.valueOf(tableVuelos.getValueAt(seleccionar, 0)));
        txtPrecio.setText(String.valueOf(tableVuelos.getValueAt(seleccionar, 1)));
        dateFechaIda.setDate(FormatoFechaDateChooser(String.valueOf(tableVuelos.getValueAt(seleccionar, 2))));
        dateFechaRegreso.setDate(FormatoFechaDateChooser(String.valueOf(tableVuelos.getValueAt(seleccionar, 3))));
        txtIdAvion.setText(String.valueOf(tableVuelos.getValueAt(seleccionar, 4)));
        txtIdRuta.setText(String.valueOf(tableVuelos.getValueAt(seleccionar, 5)));
    }//GEN-LAST:event_tableVuelosMouseClicked

